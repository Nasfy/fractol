char	*ft_strstr(const char *h, const char *n)
{
	int i;
	int u;
	int c;

	i = 0;
	u = 0;
	c = 0;
	while (n[i] != '\0')
	{
		if (h[u] == '\0')
			return (0);
		if (n[i] == h[u])
		{
			i++;
			u++;
		}
		else if (n[i] != h[u])
		{
			i = 0;
			u = c++;
		}
	}
	return (&((char *)h)[u - i]);
}
