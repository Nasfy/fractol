#include <string.h>

void	ft_striter(char *s, void (*f)(char *))
{
	size_t	i;

	if (s == 0 || (f) == 0)
		return ;
	i = 0;
	while (s[i] != '\0')
	{
		f(&s[i]);
		i++;
	}
}
